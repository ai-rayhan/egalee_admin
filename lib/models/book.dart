class Book {
  final String id;
  final String? pdflink;
  final String? price;
  final String? cod;
  final String? charge;
  final String? imagelink;
  final String? title;
  final String? writerName;

  Book({
    required this.id,
     this.pdflink,
     this.price,
     this.cod,
     this.charge,
     this.imagelink,
     this.title,
     this.writerName
  });}
